a = [0xAC, 0xF3, 0x0C, 0x25, 0xA3, 0x10, 0xB7, 0x25, 0x16, 0xC6, 0xB7, 0xBC, 0x07, 0x25, 0x02, 0xD5, 0xC6, 0x11, 0x07, 0xC5]
result = []
for _ in range(0, len(a)):
    for __ in range(33,127):
        if ((-5 * __) & 0xff == a[_]):
            result.append(__)

for i in result:
    print(chr(i), end='')